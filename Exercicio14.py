# Exercício 14: Terceiro ângulo do triângulo
# Declarar
primeiro_angulo = int(input("Digite o valor do primeiro ângulo do triângulo: "))
segundo_angulo = int(input("Digite o valor do segundo ângulo do triângulo: "))
# Início
terceiro_angulo = 180 - (primeiro_angulo + segundo_angulo)
print("O terceiro ângulo do triângulo é:", terceiro_angulo)
